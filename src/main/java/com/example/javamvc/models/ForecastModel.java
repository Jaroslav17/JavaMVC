package com.example.javamvc.models;

public class ForecastModel {
    public Integer id;
    public String city;
    public String date;
    public double temperatura;

    public ForecastModel(String city, String date, double temperatura) {
        this.id = id;
        this.city = city;
        this.date = date;
        this.temperatura = temperatura;


    }
}
